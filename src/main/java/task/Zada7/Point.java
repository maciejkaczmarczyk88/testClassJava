package task.Zada7;

public class Point {
    private int pointY;
    private int pointX;

    public Point(int pointY, int pointX) {
        this.pointY = pointY;
        this.pointX = pointX;
    }


    public Point() {
    }

    public int getPointY() {
        return pointY;
    }

    public void setPointY(int pointY) {
        this.pointY = pointY;
    }

    public int getPointX() {
        return pointX;
    }

    public void setPointX(int pointX) {
        this.pointX = pointX;
    }
}
